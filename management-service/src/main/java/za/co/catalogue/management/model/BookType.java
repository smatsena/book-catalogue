package za.co.catalogue.management.model;

/**
 * Enumeration representing the different types of books available in the catalogue.
 */
public enum BookType {
    /** Hardcover/physical book with hard cover */
    HARD_COVER,
    
    /** Paperback/physical book with soft cover */
    SOFT_COVER,
    
    /** Electronic book/digital format */
    EBOOK
}
